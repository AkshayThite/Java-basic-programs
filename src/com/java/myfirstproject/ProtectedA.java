package com.java.myfirstproject;

public class ProtectedA {

	protected String name = "John Snow";
	protected String wealth = "I will grant my wealth to the child welfare trust";

	protected void getName() {
		System.out.println("Name is " + name);
	}

	protected void getWealth() {
		System.out.println("Charity is " + wealth);
	}

}
